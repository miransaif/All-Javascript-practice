var Cities = [
    "Hyderabad",
    "Mumbai",
    "Delhi",
    69,
    "Pune",
    "jaipur"
]

// for loop
for(let i=0 ; i < Cities.length ; i++){
    console.log(Cities[i]);
}

// while loop
let i = 0;
while (i < Cities.length) {
    console.log(Cities[i]);
    i++;
}

// Do while loop

do {
    console.log(Cities[i]);
    i++;
} while (i < Cities.length);